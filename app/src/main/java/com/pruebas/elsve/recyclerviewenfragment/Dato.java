package com.pruebas.elsve.recyclerviewenfragment;

public class Dato {

    private String Texto1, Texto2;

    public Dato() {
    }

    public Dato(String texto1, String texto2) {
        Texto1 = texto1;
        Texto2 = texto2;
    }

    public String getTexto1() {
        return Texto1;
    }

    public void setTexto1(String texto1) {
        Texto1 = texto1;
    }

    public String getTexto2() {
        return Texto2;
    }

    public void setTexto2(String texto2) {
        Texto2 = texto2;
    }
}
